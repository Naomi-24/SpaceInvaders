#ifndef ENEMYMODEL_H
#define ENEMYMODEL_H

#include <ngl/Obj.h>

//----------------------------------------------------------------------------------------------------------------------
/// @file EnemyModel.h
/// @brief this class stores model for enemy ships
/// @author Naomi Morgan
/// @version 1.0
/// @date 11/05/17
/// @class EnemyModel
/// @brief stores model for enemy ships
//----------------------------------------------------------------------------------------------------------------------


class EnemyModel
{
public:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor for EnemyModel class
    /// @param
    //----------------------------------------------------------------------------------------------------------------------
    EnemyModel();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief dtor this will clear the models and remove all of the meshes created
    //----------------------------------------------------------------------------------------------------------------------
    ~EnemyModel();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief add mesh with no texture
    /// @param [in] name of the model to use for lookup
    /// @param [in] path / name of mesh
    //----------------------------------------------------------------------------------------------------------------------
    void addEnemyModel(const std::string &_name, const std::string &_mesh);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief accessor to model, if caller wants to modify mesh
    //----------------------------------------------------------------------------------------------------------------------
    ngl::Obj *getEnemyModel(std::string _name);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief method to draw mesh, all tx must be executed before call to draw
    /// @param [in] name of mesh to draw
    //----------------------------------------------------------------------------------------------------------------------
    void draw(std::string) const;

private:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief
    //----------------------------------------------------------------------------------------------------------------------
    ngl::Obj m_mesh;
};

#endif // ENEMYMODEL_H
